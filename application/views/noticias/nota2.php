                                    <p><b>Las empresas y los ODS</b> </p>

                                        

<p>Las empresas necesitan los Objetivos de Desarrollo Sustentable (ODS) porque ofrecen una estrategia de crecimiento convincente para las empresas individuales, para los negocios en general y para la economía mundial.</p>

<p>Los ODS establecen una hoja de ruta para terminar con la pobreza, reducir la desigualdad y enfrentar el cambio climático, entre otras ambiciones. Los 17 objetivos y 169 objetivos específicos de esta Agenda 2030 para el Desarrollo Sostenible llaman la atención del mundo en abordar los problemas medioambientales, sociales y económicos más importantes que enfrentamos hoy en día.</p>

<p><img src="/assets/site/img/noticias/Comunicar-los-ODS.jpg" alt=""></p>

<p>Los negocios de hecho están empezando a intensificarse. Un informe de la ONUpublicado en abril de 2017 encontró que 82 de cada 100 empresas de primer nivel demostraron su compromiso con los ODS en sus informes anuales de 2016, ya sea a través de declaraciones explícitas sobre los objetivos o las acciones implícitas que los respaldan. El ODS 13 (Acción climática), el ODS 3 (Buena salud) y el ODS 10 (Desigualdades reducidas) fueron las principales prioridades, con un enfoque clave que varía según el sector.</p>

<p><div class="alert alert-info"><blockquote>
<p>Estos son los retos para los negocios cuando se trata de implementar los ODS.</p>
</blockquote></div></p>
<p>La ambición corporativa alrededor de los ODS solo aumentará. Algunas empresas ya están reconociendo la necesidad de que las empresas operen en economías estables y, a la inversa, la creciente desiguadldad, la pobreza y el clima y los riesgos del agua son amenazas para casi cualquier modelo comercial. Los cinco principales riesgos a mediano plazo destacados en el Informe de riesgos mundiales 2016 del Foro Económico Mundial se alinean con los ODS. Si las empresas protegen sus cadenas de suministro y mercados futuros, es vital que se aborden.</p>

<p>Hay un número creciente de herramientas disponibles para ayudar a las empresas a evaluar e informar sus contribuciones hacia los ODS utilizando indicadores y métricas compartidos. Sin embargo, aún es temprano y algunas empresas están siendo víctimas de peligros comunes. Este reporte analiza las oportunidades y los desafíos clave que enfrentan las empresas, las lecciones aprendidas y las recomendaciones sobre cómo las empresas pueden mejorar las prácticas actuales y mitigar el riesgo para entregar contribuciones creíbles y significativas hacia los ODS.</p>
<p><img src="/assets/site/img/noticias/preocupaciones-en-los-proximos-10-anos.png" alt=""></p>
<p><h3>Oportunidades clave para los negocios cuando se trata de los ODS</h3>
<b>Oportunidad de mercado de 12 billones dólares</b>

<p>Los ODS prometen importantes recompensas económicas para las empresas que invierten en la entrega de soluciones innovadoras y cambios transformadores. Según un informe de la Comisión Empresarial, lograr los ODS podría crear 380 millones de empleos y ayudar a desbloquear al menos 12 billones dólares en oportunidades para los negocios para el 2030. Esto favorece a las empresas que toman medidas sobre las que se quedan atrás.</p>

<p>Un estudio de 12 compañías, incluidas Siemens, Toshiba, DuPont y Philips por la organización de investigación pública Conference Board descubrió que entre 2010 y 2013, los ingresos de productos sostenibles crecieron a un ritmo seis veces mayor que los ingresos totales de la compañía.</p>

<p>Unilever tiene marcas que han integrado la sostenibilidad tanto en su propósito como en sus productos, que crecen un 30% más rápido que el resto de sus negocios. Con la creciente evidencia de un crecimiento acelerado para productos sostenibles, no es de sorprender que empresas como L’Oréal y Philips estén llevando a cabo importantes iniciativas de sostenibilidad, o que Johnson & Johnson adquirieron recientemente dos marcas de limpieza ecológica, Method y Ecover.</p></p>

<p>
	<br/>Confianza y lealtad del cliente

Cada vez hay más evidencia de que los consumidores son cada vez más conscientes de la  sostenibilidad de las empresas y están dispuestos a gastar su dinero en esas compañías. En una encuesta de 2015 de 30,000 consumidores en 60 países, el 66% de los encuestados dijeron que estarían dispuestos a pagar más por bienes sostenibles, con factores como el medio ambiente, empaque, precio, mercadeo y reclamos sobre las credenciales orgánicas o de salud y bienestar. Con el 81% de los Millennials creyendo que las empresas tienen un papel clave en el logro de los ODS, las empresas deben demostrar un compromiso claro y creíble con la sostenibilidad si quieren mantener su lealtad como clientes y empleados.
</p>
<p><img src="/assets/site/img/noticias/confianza.jpeg" alt=""></p>
<p><p></p><b>Costos más bajos por un uso más eficiente de los recursos</b>

<p>Se prevé que para el año 2030, a medida que las energías renovables desplacen a los combustibles fósiles, sea cada vez más barato elegir eólica y solar que el carbón o el gas en países como India, Reino Unido y Dinamarca, y pronto será el caso de muchos más países. Con la inevitable transición de los combustibles fósiles, tiene sentido desde el punto de vista económico a largo plazo comenzar esta transición ahora, de ahí el reciente anuncio de muchos fabricantes de automóviles de todo el mundo de que están eliminando gradualmente los automóviles de gasolina y diésel.</p>

<p>Según RE100, 124 las principales compañías mundiales, incluidas IKEA, H&M y Google, se han comprometido a utilizar electricidad 100% renovable en los próximos años. Además de reforzar la reputación corporativa, invertir en energías renovables ahora protege contra la fluctuación de los precios de los combustibles fósiles y garantiza un suministro estable de energía en las próximas décadas. Las empresas también están invirtiendo en prácticas de gestión del agua más eficientes, además de innovar en torno a los métodos de diseño circular, que pueden reducir el desperdicio e incluso proporcionar nuevos materiales e ingresos.
Riesgo reducido</p>

<p>Las empresas no pueden tener éxito en un mundo de inestabilidad ambiental, económica y social. Abordar cuestiones importantes, como el cambio climático, la pérdida de biodiversidad, la pobreza y la igualdad de género ayudará a las empresas a asegurar cadenas de suministro resilientes y mercados estables para el crecimiento futuro. Un estudio de CDP descubrió que las empresas que gestionan y planifican activamente el cambio climático tienen un 18% más de retorno de la inversión (ROI) que las empresas de menor puntaje, lo que indica que un enfoque proactivo para administrar el riesgo vale la pena.</p>
<p><b>Valor a largo plazo en nuevos mercados</b><br/>

Además de asegurar un futuro próspero y estable para el planeta, los ODS proporcionan un marco contra el cual las empresas pueden asegurar su propio crecimiento y prosperidad en el futuro. Esto se puede hacer aumentando las inversiones en nuevos mercados con un potencial de crecimiento significativo. Con el nuevo lenguaje común, los objetivos e indicadores que proporcionan los ODS, las empresas pueden informar el progreso a las partes interesadas y diferenciar su desempeño de las empresas competidoras.
Desafíos y dificultades comunes cuando se trata de los ODS y los negocios</p>

<p>Aunque el concepto de sostenibilidad en los negocios está ganando importancia con los ODS, no es nada nuevo. Muchas compañías responsables lo han implementado a sus estrategias hace mucho tiempo. Sin embargo, los ODS elevan la barrera de lo que se espera de las empresas, a la vez que introducen un nuevo marco para hacer negocios y criterios comunes para informar el avance. Como estos objetivos aún son nuevos, crear, cuantificar y reportar impactos creíbles puede hacer que las empresas sean susceptibles a algunas dificultades comunes.</p></p>

<p><img src="/assets/site/img/noticias/reformular-comunicacion.jpg" alt=""></p>
<p>
	<b>1. Reformular las comunicaciones y mantener el negocio como hasta ahora</b><p></p>

<p>Dado que las empresas han mejorado las actividades de desarrollo sostenible y presentado informes en los últimos años, podría ser tentador para las empresas observar el buen trabajo que ya están realizando y reformular las comunicaciones para alinear sus acciones con los ODS. Un mayor reconocimiento de los ODS es algo bueno, pero alinearse y comunicarse sobre los ODS es solo un paso hacia el objetivo final de usarlos para establecer un nivel de ambición adecuado e informar las opciones estratégicas de desarrollo comercial, con el objetivo de maximizar los impactos positivos. Por lo tanto, los ODS piden a las empresas que examinen de nuevo cómo definen las mejores prácticas, establecen objetivos y miden los impactos.</p>
<b>2. Configuración de objetivos dirigida internamente</b><br/>

<p>Para aquellas empresas que han dado el paso para establecer objetivos internos para sus contribuciones a los ODS, es común establecer niveles de ambición internamente. Los objetivos internos a menudo están influenciados por factores tales como los recursos disponibles y lo que parece más factible en lugar de estar impulsados por lo que se necesita para cumplir con la Agenda 2030.</p>
<b>3. Dejar negativos y trade-offs fuera de la escena</b><br/>

<p>El éxito es la razón por la cual la mayoría de las comunicaciones relacionadas con la sostenibilidad o los ODS se centran en los impactos positivos, dejando los impactos negativos o las concesiones mutuas. Aunque este es un enfoque viable con un impacto universal como las emisiones de gases de efecto invernadero, aplicar el mismo concepto a otras preocupaciones locales o humanitarias como la salud o el acceso al agua potable es problemático, engañoso y puede socavar la confianza pública y aumentar el riesgo de reputación para las empresas.</p>
</p>
<p><div class="alert alert-info"><blockquote>
<p>Los ODS ofrecen muchas oportunidades para los negocios..</p>
</blockquote></div></p>
<p><b>4. Evaluación de entradas/salidas en lugar de resultados/impacto</b><br/>

Los dólares de sostenibilidad a menudo se canalizan a proyectos valiosos con informes posteriores sobre la cantidad de dinero gastada en una iniciativa, cuántos productos se financiaron o cuántas personas atendieron. Pero estas métricas no son indicadores confiables para resultados duraderos y sostenibles.</p>
<p><img src="/assets/site/img/noticias/ods-y-negocios.png" alt=""></p>
<p><b>5. Métodos e indicadores de informes inconsistentes</b><br/>

Con tantos factores involucrados en las iniciativas de sostenibilidad, puede ser un desafío medir e informar los impactos de una manera significativa y creíble. Sin embargo, con las expectativas de que las mejores prácticas aumenten, y los líderes en sostenibilidad pongan el listón más alto, los impactos auto-medidos y autoinformados solo serán creíbles si los métodos de cuantificación e información son transparentes, precisos y consistentes en el tiempo. Los datos deben ser cuantitativos para que el avance hacia los ODS se pueda medir y comparar adecuadamente, año tras año. Los casos de estudio, las citas y las fotos se pueden usar para complementar los informes y comunicar la ‘historia humana’ detrás de una iniciativa.</p>
<p><img src="/assets/site/img/noticias/porcentaje-de-objetivo-alcanzado.png" alt=""></p>
<p>
	<b>Mejores prácticas para la cuantificación del impacto de alta calidad y la presentación de informes con los ODS</b><br/>

    Comprometerse con la cuantificación integral de los impactos y el establecimiento de objetivos.
    Hacer un buen uso de la verificación y certificación de terceros.
    Integrar la cuantificación e informe de los ODS en sus procesos internos de toma de decisiones.

</p>

