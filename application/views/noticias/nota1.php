                                    <p><b>INTRODUTION: </b> </p>
                                        <p>Tutor Negotia's code of ethics and conduct, mission and vision was developed in accordance with international best practices in order to provide to all our collaborators, clients and stakeholders with the guidelines that govern us.</p>
                                        <p>This is how this code becomes a guide for the tasks to be carried out on a day-to-day basis by all our members as a way of relating both within the organization and with customers and society in general. Therefore, each of the members of Tutor Negotia carries out their work within responsible and fair practices, just as it is expected that each member of the organization requests the guidance that considers necessary.</p>

<p>This code is elaborated and modified by the members of the Ethics Committee of Tutor Negotia, and it is complemented with the policies defined by the organization.</p>

<p><b>Ethical Commitment</b></p>

<p>We are an intelligent company, full of vitality and professionalism, integrated by the best in each area of its performance; We are a demanding organization with the result of our work, energetic and innovative and is part of a generation that understands that prestige and respect is achieved based on extraordinary results.</p>

<p>Tutor Negotia and its associates are committed to build, maintain and sustain the trust of society and investors, our relationship with all stakeholders is based on ethical, transparent and fair behavior, taking care that our work never disrupts social and environmental interests . At all times we work on the construction of agreements and the balance of the interests of individuals in favor of sustainable and sustainable development. We will never allow the personal or third-party benefits in our work to be above the common good.</p>

<p>By accepting our Code of Ethics, all of us involved become more aware of the implications, privileges and responsibilities that come with working in a most prestigious Risk Management and Auditing firm in Mexico.</p>

<p><b> Values and principles</b></p>

<p>In Tutor Negotia values and principles are built in a co-responsible and intrinsically related way. Shared values sustain and interweave in a responsible way of the essence of our service; while ethical principles mark the direction and behavior that we expect from our people.</p>
                                        
                                        
                                        <p class="special"><b>Our Shared Values</b><br>

    <b>Strategic vision:</b> The ability to create, develop and use a concept related to the purpose of the organization.<br/>

   <b> Shared destiny:</b> The feeling of connecting the whole organization through common purpose. <br/>

    <b>Longing for change:</b> The ability to leave the past behind and changing the way things are do for the business succes. <br/>

   <b> Passion for the organization:</b> The collective feeling of energy, enthusiasm, motivation and willingness that leads to do an extra effort in order to achieve success in the organization.<br/> 

   <b> Alignment and congruence:</b> The way in which the organizational structure operates to achieve the strategic vision. <br/>

    <b>Use of knowledge: </b>The ability of organizations to capitalize on their learning, experience of its collaborators and their collective wisdom. <br/>

   <b> Performance pressure:</b> It goes beyond the encouragement exercised by managers and supervisors for staff to meet the goals. It means that everyone accepts and assumes the objectives personally, understanding how valuable it is to contribute their knowledge and work to their fulfillment. <p></p>
</p>


<p class="special"><b>Our Principles</b><br>

    <b>Honesty:</b> The exercise of all our functions is carried out within the framework of honesty and transparency in everything we do, develop and share, which we reflect on our relationship with our colleagues and with our clients.<br/>

   <b>Integrity: </b>Each representative of Tutor Negotia develops his work with rectitude, fairness and sincerity.<br/>

    <b>Stewardship:</b> We share the responsibility of all the members of Tutor Negotia for our clients and the society itself. <br/>

   <b>Objectivity and Independence:</b> Our conclusions are based on the development of impartial and independent evaluations that have sufficient evidence, free of the interests and opinions of third parties.<br/> 

  <b> Confidentiality:</b> All information provided by clients for the development of our work is reserved, so no member of Tutor Negotia is authorized to disclose such information for their own or third parties benefits, except with the prior authorization of the Client and the General Manager. <br/>

   <b> Responsibility:</b> We act with suitability and responsibility. As we fulfill the commitments acquired by which we were hired, delivering to our customers the best product. <br/>

 <b> Professional development:</b> In Tutor Negotia we design continuous training programs for our team of collaborators. We encourage internal promotion programs, in an environment of justice and equity.<br/>
  
  <b>Respect, diversity and impartiality:</b> Our culture and work environment base its pillars on Respect, Stewardship, Courtesy and Equity, all our members have the same opportunities regardless of their race, gender or religion. We promote the diversity of people, points of view, talents and experiences.<br/>
   <p></p>
</p>
                                        


<p>Guidelines</p>

<p> The ethics program defined for our organization, is based on the principles established by the norms and behaviors that all our collaborators are expected to develop.<br/>

<b>Understanding and Complying with the Code of Ethics:</b> All the employees of Tutor Negotia must understand and comply with the guidelines provided in this Code, in the same way as those assumed for the exercise of our duties according to the profession we practice, propending the good name of the profession and organization that we represent.<br/>

 The breach of any of the guidelines is considered a serious fault, which can even lead to dismissal with just cause of the organization.</p>

<p><b> Professional Conduct:</b> It is the duty of all our team of professionals to comply with the legal and ethical obligations required by the profession, the organization and the regulatory in each of the geographical areas where we are located.<br/>

 All our professionals must provide their services from the technical requirements of their profession, as well as within the framework of the ethical principles of our organization. Providing our clients with services in which our experience and knowledge allow us to meet the objectives for which we are hired, and in turn we clearly state the economic retribution of our work; Under no circumstance will we take for granted the understanding of our clients, ensuring a fair and balanced relationship between the contribution of our work and the benefits obtained.<br/>

 In no case will we commit to provide services outside the area of our knowledge.</p>

<p> <b>Confidentiality:</b> The provision of our services is based on the principle of confidentiality and therefore on the trust that our clients place in our organization to contribute to the fulfillment of their objectives, reason why all information known by our professionals is managed under the reserve levels required, technical security; with which they tell us about our work tools and information custody practices on a day-to-day basis.<br/>

 We disclose the information that our clients authorize, however, if it is required by local authorities or external control, we will proceed with the protocol of our organization for compliance with the law.</p>

<p> <b>Independence and Objectivity.</b> Our collaborators are expected to develop their work in a free and independent manner, making objective evaluations where their professional judgment is free both of third-party opinions and of conflicts of interest that affect the results of the evaluations or their communication.<br/>

 Our conclusions are based on the principle of compliance with the law and the delivery of accurate and real information of operations by our customers.</p>

 <p><b>Conflict of Interest.</b> Our professionals carry out their work based on the principle of avoiding situations of conflict of interest, for which each member of our organization must evaluate whether the solution or position of our organization in relation to a situation is within accepted norms, technical and ethically, or, if on the contrary, it represents a conflict of interest that may affect your organization, clients or you own name.<br/>

 Likewise, it is expected that our collaborators consult with the corresponding officials, any clarification, guidance or confirmation. As a result, each officer has the responsibility to report any conflict of interest or violation of internal or external legislation.</p>

<p><b> Bribes or Extortions:</b> Tutor Negotia does not allow the making of payments and / or bribes for the development of negotiations in favor of our organization or in his own name, for which it is expected that none of our officers incur in these actions.<br/>

 Likewise, the bribery of members of the government or of the regulating entities in favor of benefits is not allowed.<br/>

 Our organization will in no case pay extortion to groups outside the law, therefore, any member of the organization that is the object of the same, must inform immediately to the Executive Direction of Tutor Negotia.</p>

<p><b> Respect:</b> It is expected that all our collaborators relate in a friendly, cordial and respectful manner with their co-workers and with our clients. We respect the personal spaces of our collaborators and the cultural and opinion differences. Harassment or discrimination is not accepted or justified for any reason.</p>

<p><b>Social Responsibility:</b> We help the less favored communities, contributing voluntarily with the time of our collaborators, which allows us to develop the human dimension of our employees and work at the same time for a more just and equitable society.</p>

<p>
	Contact: In case of identifying situations that attempt against compliance with this code, or that represent a conflict of interest, violation of the law, or situations in which you require guidance on the management of a special situation, you may follow the following communication channels:<br/><br/>

    a) Ethical line available 24 hours,<br/>
    b) Immediate Superior<br/>
    c) Human Resources<br/>
    d) Communication to the Ethics Committee.<br/><br/>

       The use of each of the above channels depends on the Negotia Tutor Executive Management.<br/>

The channels for communication with the Executive Directorate are:<br/><br/>

    a) etica@tutornegotia.com<br/>
</p>
<p>
Denunciations in the ethical line will keep the name of the collaborator, however, if required to extend it, the collaborator will receive a statement from the General Direction or Executive Direction and may have anonymity in case that an investigation is required.</p>

<p>Other considerations to. This code must be disclosed to all members of the organization, through internal communications, training and other tools that support its disclosure.b. Any breach of this code will be cause for disciplinary sanctions that may even lead to dismissal with just cause.c. The code must be reviewed annually by the Ethics Committee, who will approve any modification to it.d. All officials of the organization must periodically certify the understanding of the guidelines registered in the code.and. The members of the Ethics Committee will be chosen by the Board of Directors of Tutor Negotia.F. This code is complemented by the other policies of the organization.</p>
</p>



