       <h2 class="texto_azul_parrafo2 text-left">BENEFICIOS DEL MODELO DE
<br>GESTIÓN DE TUTOR NEGOTIA</h2>
       <h5>Principios de la gestión de proyectos<br> de Tutor Negotia</h5>
       <br>
       <div class="row">
       	<div class="col-lg-3">
       		<h6><strong>Reducir el nivel de impacto<br>
en el desempeño</strong></h6>
<p class="text-justificar">El objetivo de la mayoría de las organizaciones es
absorber lo más posible los impactos en el
desempeño organizacional resultado de las
amenazas externas e internas. La gestión del
riesgo empresarial permite que las 
organizaciones se anticipen a los riesgos que 
afectarían al desempeño e implanten las medidas
necesarias para minimizar los trastornos y 
maximizar las oportunidades.</p>
<br>
<br>
<h6><strong>Mejorar la resilienda de las<br>
empresas</strong></h6>

<p class="text-justificar">La viabilidad a medio y largo plazo de una entidad
depende de su capacidad para anticiparse y
responder al cambio, no sólo para sobrevivr sino
también para evolucionar y prosperar. Esto es
posible, en parte, gracias a una gestión eficaz del
riesgo empresarial siendo cada vez más
importante a medida que se acelera el ritmo de 
cambio y aumenta  la complejidad en el entorno
empresarial.
</p>
       	</div>
       	<div class="col-lg-1"></div>
       	<div class="col-lg-3">
       		<h6><strong>Incrementar las<br> oportunidades de respuesta</strong></h6>
<p class="text-justificar">Al tener en cuenta todas las posibilidades, tanto
de impactos positivos como negativos del riesgo,
la alta dirección puede identificar nuevas
oportunidades y desafíos únicos asociados con
las oportunidades actuales. Con el adecuado
tratamiento al riesgo se tiene la oportunidad de
fortalecer las decisiones estratégicas en la toma
de decisiones para la correcta aplicación de los
recursos de la organización.</p>

<br>
<h6><strong>Alineación de las estrategias<br> con la visión y la misión</strong></h6>

<p class="text-justificar">Es fundamental para las decisiones definir lo que
se está tratando de conseguir y comó se quiere
llevar a cabo sus actividades de negocio, es
importante durante la etapa de diseño de las
estrategias entender la relevancia de gestionar el
riesgo para demostrar resiliencia durnate los 
periodos de cambio..
</p>
       	</div>
       	<div class="col-lg-1"></div>
       	<div class="col-lg-3">
       		<h6><strong>Ser eficientes con los <br> recursos</strong></h6>
<p class="text-justificar">Todo riesgo puede considerarse una petición de
recursos. Dado que los recursos son finitos, si se
dispone de una información sólida sobre riesgos,
la dirección puede evaluar las necesidades
generales de recursos, establecer prioridades y 
mejorar su asignación.</p>
<br>
<br>
<br>
<br>
<br>
<br>
<h6><strong>Identificar y gestionar el<br>riesgo en toda la empresa</strong></h6>

<p class="text-justificar">Cada entidad se enfrenta a innumerables riesgos
que pueden afectar a muchas partes de la
organización. A veces, un riesgo puede originarse
en una parte de la entidad, pero puede afectar a 
otra diferente. En consecuencia, la dirección
identifica y gestiona estos riesgos en todos los
niveles de la organización para sostener y mejorar
el desempeño.
</p>
